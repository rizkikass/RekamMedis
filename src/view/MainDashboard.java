    private void tblRMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRMMouseClicked
        int row = tblRM.getSelectedRow();
        String id_rm = tblRM.getValueAt(row, 0).toString();
        String id_registrasi = tblRM.getValueAt(row, 1).toString();
        String tgl_masuk = tblRM.getValueAt(row, 2).toString();
        String tgl_keluar = tblRM.getValueAt(row, 3).toString();
        String ruang = tblRM.getValueAt(row, 4).toString();
        String poliklinik = tblRM.getValueAt(row, 5).toString();
        String anamnesa = tblRM.getValueAt(row, 6).toString();
        String diagnosa = tblRM.getValueAt(row, 7).toString();
        String terapi = tblRM.getValueAt(row, 8).toString();
        String cara_keluar = tblRM.getValueAt(row, 9).toString();
        txtIdRegRMH.setText(id_registrasi);
        txtTglMskRMH.setText(tgl_masuk);
        txtTglKeluarRMH.setText(tgl_keluar);
        txtRuangRMH.setText(ruang);
        txtPoliklinikRMH.setText(poliklinik);
        txtAnamnesaRMH.setText(anamnesa);
        txtDiagnosaRMH.setText(diagnosa);
        txtTerapiRMH.setText(terapi);
        txtCaraKeluarRMH.setText(cara_keluar);
        PreparedStatement ps;
        try{
            ConnectionManager conMan = new ConnectionManager();
            Connection conn = conMan.logOn();
            ps = conn.prepareStatement("select * "
                    + "from registrasi r "
                    + "join pasien p on r.id_pasien = p.id_pasien "
                    + "join dokter d on r.id_dokter = d.id_dokter "
                    + "where id_registrasi=?");
            ps.setString(1, id_registrasi);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                String id_pasien = rs.getString("p.id_pasien");
                String nama_pasien = rs.getString("p.nama_pasien");
                String id_dokter = rs.getString("d.id_dokter");
                String nama_dokter = rs.getString("d.nama_dokter");
                String spesialis = rs.getString("d.spesialis");
                txtIdPasienRMH.setText(id_pasien);
                txtIdDokterRMH.setText(id_dokter);
                txtNamaPasienRMH.setText(nama_pasien);
                txtNamaDokterRMH.setText(nama_dokter);
                txtSpesialisRMH.setText(spesialis);
            }
            else{
                JOptionPane.showMessageDialog(tblRM, "Error");
            }
        }catch(SQLException sQLException){
            sQLException.printStackTrace();
        }
    }//GEN-LAST:event_tblRMMouseClicked

