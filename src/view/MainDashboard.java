        String cara_pembayaran = tblRegistrasi.getValueAt(row, 5).toString();
        String cara_masuk = tblRegistrasi.getValueAt(row, 6).toString();
        String poliklinik = tblRegistrasi.getValueAt(row, 7).toString();
        String triase = tblRegistrasi.getValueAt(row, 8).toString();
        txtIdPasienRegH.setText(id_pasien);
        txtIdAdminRegH.setText(id_admin);
        txtIdDokterRegH.setText(id_dokter);
        txtTglRegH.setText(tgl_regis);
        txtCaraPembayaranRegH.setText(cara_pembayaran);
        txtCaraMasukRegH.setText(cara_masuk);
        txtPoliklinikRegH.setText(poliklinik);
        txtTriaseRegH.setText(triase);
        PreparedStatement ps;
        try{
            ConnectionManager conMan = new ConnectionManager();
            Connection conn = conMan.logOn();
            ps = conn.prepareStatement("select * "
                    + "from registrasi r "
                    + "join pasien p on r.id_pasien = p.id_pasien "
                    + "join dokter d on r.id_dokter = d.id_dokter "
                    + "join admin a on r.id_admin = a.id_admin "
                    + "where id_registrasi=?");
            ps.setString(1, id_registrasi);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                String nama_pasien = rs.getString("p.nama_pasien");
                String nama_admin = rs.getString("a.nama_admin");
                String nama_dokter = rs.getString("d.nama_dokter");
                String spesialis = rs.getString("d.spesialis");
                txtNamaPasienRegH.setText(nama_pasien);
                txtNamaAdminRegH.setText(nama_admin);
                txtNamaDokterRegH.setText(nama_dokter);
                txtSpesialisRegH.setText(spesialis);
            }
            else{
                JOptionPane.showMessageDialog(tblRegistrasi, "Error");
            }
        }catch(SQLException sQLException){
            sQLException.printStackTrace();
        }
